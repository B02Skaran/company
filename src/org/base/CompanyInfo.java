package org.base;

import java.util.*;

public class CompanyInfo {
	
	private int empId;
	private String name;
	private long mobile;
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public long getMobile() {
		return mobile;
	}
	public void setMobile(long mobile) {
		this.mobile = mobile;
	}
	
	
	
	

	
	
	
	
	
	
	
	
}
